x = 5
y = 5

if x == y:								#if 'x' and 'y' have the same value then:
	print("x and y are the same")
elif x > y:								#or if 'x' is bigger then 'y'
	print("x is bigger than y")
else:									#in any other case:
	print("x is smaller than y")


